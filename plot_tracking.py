import cv2
import numpy as np

#Takes a filename and a frame id, returns all bounding boxes and scores in that frame.
#tid = target ID
def get_bbox(file, frame_id):
    frame_ids = []
    tids = []
    tlwhs_lst = []
    scores = []
    with open(file, 'r') as f:
        for line in f:
            line = line.strip()
            values = line.split(',')
            if (not values[0].isdigit()) or int(values[0])!=frame_id:
                continue
            frame_id = int(values[0])
            tid = int(values[1])
            tlwhs = []
            for i in values[2:6]:
                tlwhs.append(float(i))
            score = float(values[6])
            frame_ids.append(frame_id)
            tids.append(tid)
            tlwhs_lst.append(tlwhs)
            scores.append(score)
    #All return types are lists
    return frame_ids, tids, tlwhs_lst, scores

def vis(img, boxes, scores, cls_ids, conf=0.5, class_names=None):

    for i in range(len(boxes)):
        box = boxes[i]
        cls_id = int(cls_ids[i])
        score = scores[i]
        if score < conf:
            continue
        x0 = int(box[0])
        y0 = int(box[1])
        x1 = int(box[2])
        y1 = int(box[3])

        color = (_COLORS[cls_id] * 255).astype(np.uint8).tolist()
        text = '{}:{:.1f}%'.format(class_names[cls_id], score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX

        txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
        cv2.rectangle(img, (x0, y0), (x1, y1), color, 2)

        txt_bk_color = (_COLORS[cls_id] * 255 * 0.7).astype(np.uint8).tolist()
        cv2.rectangle(
            img,
            (x0, y0 + 1),
            (x0 + txt_size[0] + 1, y0 + int(1.5*txt_size[1])),
            txt_bk_color,
            -1
        )
        cv2.putText(img, text, (x0, y0 + txt_size[1]), font, 0.4, txt_color, thickness=1)

    return img



def get_color(idx):
    idx = idx * 3
    color = ((37 * idx) % 255, (17 * idx) % 255, (29 * idx) % 255)

    return color

